import math

angulo = float(input('Digite um um ângulo: '))
print('Para o ângulo {}, o valor de seno é {}, de cosseno é {} e o valor da tangente é {}'.format(angulo,
                                                                                                  math.sin(angulo),
                                                                                                  math.cos(angulo),
                                                                                                  math.tan(angulo)))
